<script>
  function toggleFaq(element) {
    const faqItem = element.parentElement;
    const answer = element.nextElementSibling;
    const isExpanded = element.getAttribute('aria-expanded') === 'true';

    // 모든 FAQ 닫기
    document.querySelectorAll('.faq-question').forEach(q => {
      q.setAttribute('aria-expanded', 'false');
      if (q.nextElementSibling) q.nextElementSibling.style.maxHeight = '0';
    });

    // 클릭한 FAQ 열기
    if (!isExpanded) {
      element.setAttribute('aria-expanded', 'true');
      answer.style.maxHeight = answer.scrollHeight + 'px';
    }

    // transition이 완료된 후 스크롤
    setTimeout(() => {
      const headerOffset = 120;
      const faqItemRect = faqItem.getBoundingClientRect();
      const scrollToPosition = faqItemRect.top + window.scrollY - headerOffset;
      
      window.scrollTo({
        top: scrollToPosition,
        behavior: 'smooth'
      });
    }, 600); // transition 시간(0.6s) + 여유
  }

    // 키보드 접근성
    document.querySelectorAll('.faq-question').forEach(question => {
        question.addEventListener('keypress', function (e) {
            if (e.key === 'Enter' || e.key === ' ') {
                e.preventDefault();
                this.click();
            }
        });
    });
</script>
